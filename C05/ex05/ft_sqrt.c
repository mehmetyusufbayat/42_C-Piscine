int	ft_sqrt(int nb)
{
	int	sqr;

	sqr = 1;
	while (sqr * sqr <= nb)
	{
		if (sqr >= 46341)
			return (0);
		else if (sqr * sqr == nb)
			return (sqr);
		sqr++;
	}
	return (0);
}
